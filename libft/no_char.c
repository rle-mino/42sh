#include "libft.h"

char	*no_char(char *s, char c)
{
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (s + i + 1);
		i++;
	}
	return (s);
}
